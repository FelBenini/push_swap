#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*sptr;
	size_t	i;

	sptr = (char *)s;
	i = 0;
	while (i < n)
	{
		sptr[i] = c;
		i++;
	}
	return (sptr);
}
